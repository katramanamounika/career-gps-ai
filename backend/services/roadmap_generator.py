def generate_roadmap(missing_skills):

    roadmap = []

    week = 1

    for skill in missing_skills:

        roadmap.append({
            "week": week,
            "skill": skill,
            "task": f"Learn {skill} fundamentals",
            "resources": RESOURCES.get(
                skill,
                [
                    {
                        "title": f"{skill} Learning Resource",
                        "url": "https://www.google.com"
                    }
                ]
            )
        })

        week += 1

    return roadmap